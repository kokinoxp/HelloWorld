public class Dot {
    private int codY;
    private int codX;

    public Dot(int codY, int codX) {
        this.codY = codY;
        this.codX = codX;
    }

    public int getCodY() {
        return codY;
    }

    public void setCodY(int codY) {
        this.codY = codY;
    }

    public int getCodX() {
        return codX;
    }

    public void setCodX(int codX) {
        this.codX = codX;
    }
}
